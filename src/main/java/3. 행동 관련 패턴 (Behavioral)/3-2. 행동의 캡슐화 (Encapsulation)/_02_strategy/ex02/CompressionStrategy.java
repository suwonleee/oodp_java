package org._02_strategy.ex02;

/**
 * CompressionStrategy - 압축 전략 인터페이스
 *
 * Strategy 패턴의 핵심 인터페이스로, 모든 압축 알고리즘이 구현해야 하는
 * 공통 계약을 정의합니다. 다양한 압축 방식을 동일한 인터페이스로
 * 사용할 수 있게 해주는 추상화 계층입니다.
 *
 * 데이터의 특성이나 압축 요구사항에 따라 최적의 압축 알고리즘을
 * 선택하여 사용할 수 있도록 설계되었습니다.
 */
interface CompressionStrategy {
    /**
     * 데이터를 압축하는 메서드
     *
     * 각 압축 전략이 자신만의 알고리즘으로 이 메서드를 구현하여
     * 데이터를 압축합니다. 알고리즘의 구체적인 구현은 각 전략 클래스에서
     * 담당하므로, 새로운 압축 방식 추가가 용이합니다.
     *
     * @param data 압축할 원본 데이터
     * @return 압축된 데이터 문자열
     */
    String compress(String data);
}