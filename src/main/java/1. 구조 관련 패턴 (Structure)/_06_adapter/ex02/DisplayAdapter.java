package org._06_adapter.ex02;

/**
 * DisplayAdapter - 통합 디스플레이 인터페이스 (Target)
 *
 * Adapter 패턴에서 Target 역할을 담당하는 인터페이스입니다.
 * 다양한 디스플레이 연결 방식(VGA, HDMI, USB 등)을 통합하여
 * 하나의 일관된 인터페이스를 제공합니다.
 *
 * 이 인터페이스는 클라이언트가 디스플레이 연결 방식의 세부사항을
 * 몰라도 되도록 추상화 계층을 제공합니다.
 */
interface DisplayAdapter {
    /**
     * 디스플레이 출력 수행
     *
     * 연결 방식에 관계없이 통일된 디스플레이 출력 메서드를 제공합니다.
     * 각 어댑터 구현체는 이 메서드를 통해 자신만의 연결 방식으로
     * 디스플레이를 활성화합니다.
     *
     * 추상화의 이점:
     * - 클라이언트는 연결 방식을 몰라도 됨
     * - 새로운 연결 방식 추가가 용이함
     * - 일관된 사용법으로 코드 가독성 향상
     */
    void display();
}